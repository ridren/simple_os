%macro sound_macro_play_beep 1
	mov 	al, 0xb6
	out 	0x43, al
	mov 	ax, %1
	out 	0x42, al
	shr 	ax, 0x8
	out 	0x42, al

	in 		al, 0x61
	or 		al, 0b11
	out 	0x61, al
%endmacro

%macro sound_macro_turn_off 0
	in  	al, 0x61
	and 	al, ~0b11
	out 	0x61, al
%endmacro

